import L from 'leaflet';

export const getMarkerIcon = (
  transportMode: string | undefined,
  isStopPlace: boolean,
  isSelected: boolean,
) => {
  switch (transportMode) {
    case 'onstreetBus':
    case 'bus':
      return busIcon(isStopPlace, isSelected);
    case 'rail':
      return railIcon(isStopPlace, isSelected);
    default:
      return isStopPlace
        ? generalStopPlaceIcon
        : isSelected
          ? selectedGeneralQuayIcon
          : generalQuayIcon;
  }
};

const getIconColor = (isStopPlace: boolean, isSelected: boolean) => {
  return isStopPlace ? 'black' : isSelected ? '#1777F8' : '#6e7781';
};

