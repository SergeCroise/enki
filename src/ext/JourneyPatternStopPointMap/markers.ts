export const getMarkerIcon = (
  transportMode: string | undefined,
  isStopPlace: boolean,
  isSelected: boolean,
) => {
  switch (transportMode) {
    case VEHICLE_MODE.BUS:
      return isStopPlace
        ? busStopPlaceIcon(isSelected)
        : busQuayIcon(isSelected);
    case VEHICLE_MODE.RAIL:
      return isStopPlace
        ? railStopPlaceIcon(isSelected)
        : railQuayIcon(isSelected);
    default:
      return isStopPlace
        ? generalStopPlaceIcon
        : isSelected
          ? selectedGeneralQuayIcon
          : generalQuayIcon;
  }
};

